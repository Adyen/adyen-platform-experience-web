export const getPaymentLinkItemsByStatusGroup = (status: IPayByLinkStatusGroup): IPaymentLinkItem[] => {
    let allPaymentLinks;
    switch (status) {
        case 'active':
            allPaymentLinks = PAYMENT_LINKS.filter(link => ['active', 'paymentPending'].includes(link.linkInformation.status));
        case 'inactive':
            allPaymentLinks = PAYMENT_LINKS.filter(link => ['completed', 'expired'].includes(link.linkInformation.status));
        default:
            allPaymentLinks = PAYMENT_LINKS;
    }

    return allPaymentLinks.map(
        (link): IPaymentLinkItem => ({
            amount: link.linkInformation.amount,
            creationDate: link.linkInformation.creationDate,
            expirationDate: link.linkInformation.expirationDate,
            linkType: link.linkInformation.linkType,
            merchantReference: link.linkInformation.merchantReference,
            paymentLinkId: link.linkInformation.paymentLinkId,
            shopperEmail: link.shopperInformation?.shopperEmail,
            status: link.linkInformation.status,
            storeCode: link.linkInformation.storeCode,
        })
    );
};

export const getPaymentLinkDetails = (paymentLinkId: string): IPaymentLinkDetails | undefined => {
    return PAYMENT_LINKS.find(link => link.linkInformation.paymentLinkId === paymentLinkId);
};

export const expirePaymentLink = (paymentLinkId: string): void => {
    const paymentLink = PAYMENT_LINKS.find(link => link.linkInformation.paymentLinkId === paymentLinkId);
    if (!paymentLink) {
        throw 'Payment link not found';
    }

    paymentLink.linkInformation.status = 'expired';

    if (!paymentLink.paymentLinkActivities) {
        paymentLink.paymentLinkActivities = [];
    }
    paymentLink.paymentLinkActivities.unshift({
        date: new Date().toISOString(),
        type: 'expiredAction',
        expirationReason: 'manuallyExpired',
    });
};

